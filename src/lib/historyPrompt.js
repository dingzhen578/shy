export function buildHistoryPrompt(question) {
  const normalizedQuestion = String(question ?? "").trim();

  return `
请把下面这道历史主观题整理成“历史学姐笔记”风格的高考答案。

题目：
${normalizedQuestion}

内容要求：
- 必须使用中文。
- 简洁、规范、分点清晰，符合高考历史主观题答题风格。
- 便于背诵，适合高中生复习，逻辑完整，使用历史学科术语。
- 突出因果逻辑、阶段特征和评价角度。
- 不要长篇大论，不要说“作为 AI”，不要输出无关解释。
- 不要空洞鸡汤，不要编造明显错误史实。
- 每一部分整体控制在 80-140 字以内。
- 每一部分最多 2 个分点，每个分点只写 1 句。
- 不要堆砌人物、机构或年份，优先写得分关键词和因果逻辑。
- 如果题目不适合某一栏，可以写“此题重点不在该角度”。
- 使用 Markdown 输出，允许使用加粗关键词。

固定结构：

## 🌟 背景
- 用 1-2 点概括时代背景、社会矛盾、政治经济条件或思想环境。

## 📌 措施
- 用 1-2 点概括相关政策、制度、行动、做法或主要内容。

## 📝 影响
- 用 1-2 点从政治、经济、思想文化、社会或国际关系等角度分析。

## ⚠️ 局限性
- 用 1-2 点客观指出历史条件、阶级立场、制度环境或实践不足。

## ✨ 历史意义
- 用 1-2 点上升到历史发展趋势、国家发展、社会转型或复习启示层面总结。
`.trim();
}
